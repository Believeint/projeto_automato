<?php

// HELPER: MANIPULAR ENTRADAS
class Input
{
    // VERIFICA SE EXISTE DADOS NOS FORMULÁRIOS
    public static function exists($type = 'post')
    {
        switch ($type) {
            case 'post':
                return (!empty($_POST)) ? true : false;
                break;
            case 'get':
                return (!empty($_GET)) ? true : false;
                break;
            default:
                return false;
                break;
        }
    }

    // RETORNA UM ITEM GET OU POST PELO NOME
    public static function get($item)
    {
        if (isset($_POST[$item])) {
            return $_POST[$item];
        } elseif (isset($_GET[$item])) {
            return $_GET[$item];
        }
        return "";
    }

}
